package org.yx.db.sql;

import java.util.List;

public class InsertResult {
	private int effectCount;
	private List<Long> autoGeneratedKeys;

	public InsertResult(int effectCount, List<Long> autoGeneratedKeys) {
		this.effectCount = effectCount;
		this.autoGeneratedKeys = autoGeneratedKeys;
	}

	public int getEffectCount() {
		return effectCount;
	}

	public List<Long> getAutoGeneratedKeys() {
		return autoGeneratedKeys;
	}

	public long getAutoGeneratedKey() {
		if (autoGeneratedKeys == null || autoGeneratedKeys.isEmpty()) {
			return -1;
		}
		Long row = autoGeneratedKeys.get(0);
		return row == null ? -1 : row.longValue();
	}
}
