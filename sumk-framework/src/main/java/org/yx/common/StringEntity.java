package org.yx.common;

import java.util.Objects;

/**
 * 这个对象是不可修改的
 */
public final class StringEntity<T> {
	private final String key;
	private final T value;

	private StringEntity(String key, T value) {
		this.key = Objects.requireNonNull(key);
		this.value = value;
	}

	public static <T> StringEntity<T> create(String key, T value) {
		return new StringEntity<>(key, value);
	}

	public String key() {
		return key;
	}

	public T value() {
		return value;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((key == null) ? 0 : key.hashCode());
		result = prime * result + ((value == null) ? 0 : value.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		StringEntity<?> other = (StringEntity<?>) obj;
		if (key == null) {
			if (other.key != null)
				return false;
		} else if (!key.equals(other.key))
			return false;
		if (value == null) {
			if (other.value != null)
				return false;
		} else if (!value.equals(other.value))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "[key=" + key + ", value=" + value + "]";
	}

}
